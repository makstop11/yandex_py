# Zip в Python — это встроенная функция, которая позволяет объединить элементы из нескольких итерируемых объектов в один.
# Она принимает на входе два или более итерируемых объекта и возвращает итератор, который генерирует кортежи, содержащие
# элементы из всех переданных итерируемых объектов. 1
# Основная цель zip — облегчить параллельную итерацию по нескольким последовательностям. Она создаёт новую
# последовательность кортежей, где первый элемент каждого кортежа содержит первые элементы из переданных итерируемых
# объектов, второй элемент — вторые и так далее.

