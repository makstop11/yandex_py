class TriangleChecker:
    def __init__(self, a, b, c):
        self.a = a
        self.b = b
        self.c = c

    def is_triangle(self):
        if self.a != int or self.b != int or self.c != int:
            return 1

    if (self.a == self.b == self.c) or (self.a == self.b or self.a == self.c or self.b == self.c):
        return 3
